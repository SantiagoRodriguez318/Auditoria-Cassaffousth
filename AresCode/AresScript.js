function toggleInfoWindow(){
    const InfoWindow = document.getElementById("InfoWindow");
    InfoWindow.style.display = InfoWindow.style.display ==="block" ? "none" : "block";
}
function toggleSocialWindow(){
    const SocialWindow = document.getElementById("SocialWindow");
    SocialWindow.style.display = SocialWindow.style.display ==="block" ? "none" : "block";
}